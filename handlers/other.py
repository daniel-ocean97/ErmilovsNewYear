    """–ò–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –æ –ø–ª–∞–Ω–∏—Ä–æ–≤—â–∏–∫–µ"""
    if not is_admin(message.from_user.id):
        await message.answer("‚ùå –≠—Ç–∞ –∫–æ–º–∞–Ω–¥–∞ –¥–æ—Å—Ç—É–ø–Ω–∞ —Ç–æ–ª—å–∫–æ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä—É")
        return
    
    try:
        info = await scheduler.get_schedule_info()
        text = (
            f"üìä –ò–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –æ –ø–ª–∞–Ω–∏—Ä–æ–≤—â–∏–∫–µ:\n\n"
            f"–í—Å–µ–≥–æ –∑–∞–¥–∞—á: {info['total_tasks']}\n"
            f"–ê–∫—Ç–∏–≤–Ω—ã—Ö –∑–∞–¥–∞—á: {info['active_tasks']}\n"
            f"–¢–µ—Å—Ç–æ–≤—ã–π —Ä–µ–∂–∏–º: {'–î–∞' if info['is_test_mode'] else '–ù–µ—Ç'}\n"
            f"–¢–µ–∫—É—â–∏–π –≥–æ–¥: {info['current_year']}"
        )
        await message.answer(text)
    except Exception as e:
        await message.answer(f"‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏: {e}")


